
public class CalculadoraEstatica {
	public static int soma(int parcelaA, int parcelaB) {
		return parcelaA + parcelaB;
	}
	public static int subtrair(int minuendo, int subtraendo) {
		return minuendo - subtraendo;
	}
	public static double multiplicar(double multiplicando, double multiplicador) {
		return multiplicando * multiplicador;
	}
	public static double divisao(double dividendo, double divisor) {
		return dividendo / divisor;
	}
}
