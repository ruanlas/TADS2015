
public class CalculadoraInstancia {
	public int soma(int parcelaA, int parcelaB) {
		return parcelaA + parcelaB;
	}
	public int subtrair(int minuendo, int subtraendo) {
		return minuendo - subtraendo;
	}
	public double multiplicar(double multiplicando, double multiplicador) {
		return multiplicando * multiplicador;
	}
	public double divisao(double dividendo, double divisor) {
		return dividendo / divisor;
	}
}
