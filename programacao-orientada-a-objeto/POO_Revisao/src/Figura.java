
public class Figura {
	private String cor;
	private int x;
	private int y;
	
	public Figura(String cor, int x, int y){
		this.cor = cor;
		this.x = x;
		this.y = y;
	}

	public String getCor() {
		return cor;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
}
