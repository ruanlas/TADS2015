#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main()
{
    int a, x,y,z;
    x=0;
    y=0;
    z=0;
    scanf("%d", &a);
switch(a) {
case 1: ;
case 2: ;
case 3: x++; break;
case 4: y++; break;
case 5: z++; break;
}
printf("X = %d, Y = %d, Z = %d\n", x,y,z);
return 0;
}
